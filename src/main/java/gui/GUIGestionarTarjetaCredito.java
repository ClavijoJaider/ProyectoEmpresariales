    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (txtInputNumCuenta.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese un número");
                return;
            }
            int numBuscar = Integer.parseInt(txtInputNumCuenta.getText());
            CuentaBancaria cuentaGen = service.buscarPorNumero(numBuscar);

            if (cuentaGen instanceof CuentaCorriente && cuentaGen.getEstado().equalsIgnoreCase("Activo")) {
                CuentaCorriente cuenta = (CuentaCorriente) cuentaGen;
                txtNumCuenta.setText(String.valueOf(cuenta.getNumeroCuenta()));
                txtTitular.setText(cuenta.getTitular());
                TarjetaCredito tarjeta = cuenta.getTarjetaCredito();
                if (tarjeta != null) {
                    txtNumeroTarjeta.setText(tarjeta.getNumeroTarjeta());
                    txtCupo.setText(String.valueOf(tarjeta.getCupo()));
                    txtCuotaManejo.setText(String.valueOf(tarjeta.getCuotaManejo()));
                } else {
                    txtNumeroTarjeta.setText("");
                    txtCupo.setText("");
                    txtCuotaManejo.setText("");
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Cuenta no encontrada o no es de tipo Corriente");
                limpiarCampos();
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Ingrese solo números");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cuenta no encontrada");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String numCuentaStr = txtNumCuenta.getText();
        if (numCuentaStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Primero debe buscar una Cuenta Corriente.");
            return;
        }
        try {
            int numero = Integer.parseInt(numCuentaStr);
            String numTarjeta = txtNumeroTarjeta.getText().trim();
            double cupo = Double.parseDouble(txtCupo.getText());
            double cuotaManejo = Double.parseDouble(txtCuotaManejo.getText());

            if (numTarjeta.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "El número de tarjeta no puede estar vacío.");
                return;
            }

            CuentaBancaria c = service.buscarPorNumero(numero);
            CuentaCorriente corriente = (CuentaCorriente) c;

            if (corriente.getTarjetaCredito() == null) {
                service.asociarTarjetaCredito(numero, numTarjeta, cupo, cuotaManejo);
                javax.swing.JOptionPane.showMessageDialog(this, "Tarjeta asociada exitosamente.");
            } else {
                service.actualizarTarjetaCredito(numero, numTarjeta, cupo, cuotaManejo);
                javax.swing.JOptionPane.showMessageDialog(this, "Tarjeta actualizada exitosamente.");
            }
            ServicioGUI.getInstance().cambioEnGUI();
            limpiarCampos();
            txtInputNumCuenta.setText("");
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Revisa que los campos numéricos sean correctos.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTarjetaActionPerformed
        String numCuentaStr = txtNumCuenta.getText();
        if (numCuentaStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Primero debe buscar una Cuenta Corriente.");
            return;
        }
        int respuesta = javax.swing.JOptionPane.showConfirmDialog(this,
                "¿Está seguro de que desea eliminar la tarjeta de " + txtTitular.getText() + "?",
                "Confirmar eliminación",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);

        if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
            try {
                int numero = Integer.parseInt(numCuentaStr);
                service.eliminarTarjetaCredito(numero);
                ServicioGUI.getInstance().cambioEnGUI();
                javax.swing.JOptionPane.showMessageDialog(this, "Tarjeta eliminada exitosamente.");
                limpiarCampos();
                txtInputNumCuenta.setText("");
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarTarjetaActionPerformed

