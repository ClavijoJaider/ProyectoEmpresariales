    private void cargarTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        List<CuentaBancaria> todas = service.listar();
        for (CuentaBancaria cuenta : todas) {
            if (cuenta instanceof CuentaCorriente corriente && corriente.getTarjetaCredito() != null) {
                TarjetaCredito t = corriente.getTarjetaCredito();
                model.addRow(new Object[]{
                    corriente.getNumeroCuenta(),
                    corriente.getTitular(),
                    t.getNumeroTarjeta(),
                    t.getCupo(),
                    t.getCuotaManejo()
                });
            }
        }
    }

    private void limpiarCampos() {
        txtNumCuenta.setText("");
        txtTitular.setText("");
        txtNumeroTarjeta.setText("");
        txtCupo.setText("");
        txtCuotaManejo.setText("");
    }

